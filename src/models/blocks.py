"""
src/models/blocks.py
====================

Reusable PyTorch building blocks for the anomaly-detection architectures
generated by ``src.models.model_factory``. Every block in this module is
designed to be:

- *Quantization-friendly*: no in-place ops on residual paths, no
  `ConvTranspose2d` in the default upsample path, BN foldable into a
  preceding `Conv2d`.
- *TensorRT-friendly*: bilinear upsample, fixed scale factors, no
  Python control flow inside ``forward``.
- *Genome-friendly*: a single normalized config (``norm`` / ``activation``
  / ``kernel_size`` / ...) drives every block, so an NSGA-II encoder can
  flip layer types without bespoke wiring.

Layer catalogue
---------------
- :class:`ConvBNAct`             – Conv2d → Norm → Activation.
- :class:`DepthwiseSeparableConv` – Depthwise + Pointwise (MobileNetV1).
- :class:`InvertedResidual`      – MobileNetV2 inverted-residual block.
- :class:`ResidualBlock`         – ResNet-style basic / bottleneck block.
- :class:`SEBlock`               – Squeeze-and-Excitation channel attention.
- :class:`ECABlock`              – Efficient Channel Attention (1-D conv).
- :class:`SpatialAttention`      – Mean+Max pool → 7×7 conv → sigmoid.
- :class:`CBAMLite`              – SE + SpatialAttention (sequential).
- :class:`UpsampleConv`          – Bilinear upsample + Conv refinement.
- :class:`PixelShuffleUp`        – Sub-pixel convolution upsample.
- :class:`ConvTransposeUp`       – Transposed-conv upsample (fallback).

Helper builders
---------------
- :func:`make_norm`        – Build a 2-D normalization layer by name.
- :func:`make_activation`  – Build an activation by name.
- :func:`make_conv_block`  – Dispatch ``conv | ds | ir`` block flavors.
- :func:`make_attention`   – Dispatch ``se | eca | spatial | cbam | none``.

All blocks accept ``in_channels``/``out_channels`` and the relevant
hyperparameters as plain ``int``/``str`` arguments — no nested config
dicts — so they're trivial to compose programmatically.
"""

from __future__ import annotations

from typing import Literal

import torch
import torch.nn as nn
import torch.nn.functional as F

__all__ = [
    "make_norm",
    "make_activation",
    "make_conv_block",
    "make_attention",
    "ConvBNAct",
    "DepthwiseSeparableConv",
    "InvertedResidual",
    "ResidualBlock",
    "SEBlock",
    "ECABlock",
    "SpatialAttention",
    "CBAMLite",
    "UpsampleConv",
    "PixelShuffleUp",
    "ConvTransposeUp",
]

NormKind = Literal["batch", "instance", "group", "none"]
ActKind = Literal["relu", "leaky_relu", "gelu", "silu", "hardswish", "none"]
ConvKind = Literal["conv", "ds", "ir"]
AttnKind = Literal["se", "eca", "spatial", "cbam", "none"]


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
def make_norm(channels: int, kind: NormKind = "batch") -> nn.Module:
    """Instantiate a 2-D normalization layer by name.

    ``"group"`` picks an automatic group count of ``min(32, channels // 4)``
    that always divides ``channels``.
    """
    if kind == "batch":
        return nn.BatchNorm2d(channels)
    if kind == "instance":
        return nn.InstanceNorm2d(channels, affine=True)
    if kind == "group":
        groups = max(1, min(32, channels // 4))
        # Ensure groups divides channels.
        while channels % groups != 0 and groups > 1:
            groups -= 1
        return nn.GroupNorm(groups, channels)
    if kind == "none":
        return nn.Identity()
    raise ValueError(f"unknown norm: {kind!r}")


def make_activation(kind: ActKind = "relu",
                    inplace: bool = False) -> nn.Module:
    """Instantiate an activation by name.

    ``inplace`` is only honored for activations that support it; it is
    *off by default* because in-place activations can break residual
    paths during backprop and confuse some quantization backends.
    """
    if kind == "relu":
        return nn.ReLU(inplace=inplace)
    if kind == "leaky_relu":
        return nn.LeakyReLU(0.1, inplace=inplace)
    if kind == "gelu":
        return nn.GELU()
    if kind == "silu":
        return nn.SiLU(inplace=inplace)
    if kind == "hardswish":
        return nn.Hardswish(inplace=inplace)
    if kind == "none":
        return nn.Identity()
    raise ValueError(f"unknown activation: {kind!r}")


def _check_kernel(kernel_size: int) -> int:
    if kernel_size < 1 or kernel_size % 2 == 0:
        raise ValueError(f"kernel_size must be a positive odd int; "
                         f"got {kernel_size}")
    return kernel_size


# ---------------------------------------------------------------------------
# Convolutional blocks
# ---------------------------------------------------------------------------
class ConvBNAct(nn.Module):
    """Conv2d → Norm → Activation.

    Bias is automatically disabled when a normalization layer is used
    (BN/IN/GN folds away the constant), and re-enabled when ``norm="none"``.
    """

    def __init__(self,
                 in_channels: int,
                 out_channels: int,
                 kernel_size: int = 3,
                 stride: int = 1,
                 groups: int = 1,
                 dilation: int = 1,
                 norm: NormKind = "batch",
                 activation: ActKind = "relu",
                 use_act: bool = True,
                 padding_mode: str = "zeros") -> None:
        super().__init__()
        _check_kernel(kernel_size)
        pad = (kernel_size // 2) * dilation
        self.conv = nn.Conv2d(in_channels, out_channels,
                              kernel_size=kernel_size, stride=stride,
                              padding=pad, dilation=dilation, groups=groups,
                              bias=(norm == "none"),
                              padding_mode=padding_mode)
        self.norm = make_norm(out_channels, norm)
        self.act = make_activation(activation) if use_act else nn.Identity()

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.act(self.norm(self.conv(x)))


class DepthwiseSeparableConv(nn.Module):
    """Depthwise (groups=in_ch) followed by 1×1 pointwise conv.

    A residual shortcut is added automatically when ``in_channels ==
    out_channels`` and ``stride == 1`` (controllable via ``residual``).
    """

    def __init__(self,
                 in_channels: int,
                 out_channels: int,
                 kernel_size: int = 3,
                 stride: int = 1,
                 norm: NormKind = "batch",
                 activation: ActKind = "relu",
                 residual: bool | None = None) -> None:
        super().__init__()
        self.dw = ConvBNAct(in_channels, in_channels,
                            kernel_size=kernel_size, stride=stride,
                            groups=in_channels, norm=norm,
                            activation=activation)
        self.pw = ConvBNAct(in_channels, out_channels,
                            kernel_size=1, stride=1,
                            norm=norm, activation=activation)
        if residual is None:
            residual = (stride == 1 and in_channels == out_channels)
        self.use_residual = residual

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        out = self.pw(self.dw(x))
        return out + x if self.use_residual else out


class InvertedResidual(nn.Module):
    """MobileNetV2 inverted-residual block.

    Pipeline: 1×1 expand → k×k depthwise → 1×1 project (no activation).
    Adds a skip connection when ``stride == 1`` and channels match.
    """

    def __init__(self,
                 in_channels: int,
                 out_channels: int,
                 kernel_size: int = 3,
                 stride: int = 1,
                 expansion: int = 4,
                 norm: NormKind = "batch",
                 activation: ActKind = "relu") -> None:
        super().__init__()
        if expansion < 1:
            raise ValueError(f"expansion must be >= 1; got {expansion}")
        hidden = max(in_channels * expansion, 8)
        self.use_skip = (stride == 1 and in_channels == out_channels)

        layers: list[nn.Module] = []
        if expansion != 1:
            layers.append(ConvBNAct(in_channels, hidden, kernel_size=1,
                                    stride=1, norm=norm,
                                    activation=activation))
        layers += [
            ConvBNAct(hidden, hidden, kernel_size=kernel_size,
                      stride=stride, groups=hidden, norm=norm,
                      activation=activation),
            ConvBNAct(hidden, out_channels, kernel_size=1, stride=1,
                      norm=norm, activation=activation, use_act=False),
        ]
        self.block = nn.Sequential(*layers)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        out = self.block(x)
        return out + x if self.use_skip else out


class ResidualBlock(nn.Module):
    """ResNet-style residual block (basic or bottleneck).

    - ``bottleneck=False``: 3×3 → 3×3, identity / 1×1 shortcut.
    - ``bottleneck=True``:  1×1 reduce → 3×3 → 1×1 expand (×4), identity /
      1×1 shortcut. Channel-reduction factor is fixed at 4 (ResNet
      convention); override via ``bottleneck_ratio`` if needed.
    """

    def __init__(self,
                 in_channels: int,
                 out_channels: int,
                 kernel_size: int = 3,
                 stride: int = 1,
                 norm: NormKind = "batch",
                 activation: ActKind = "relu",
                 bottleneck: bool = False,
                 bottleneck_ratio: int = 4) -> None:
        super().__init__()
        _check_kernel(kernel_size)
        self.act_out = make_activation(activation)

        if bottleneck:
            mid = max(out_channels // bottleneck_ratio, 8)
            self.conv = nn.Sequential(
                ConvBNAct(in_channels, mid, kernel_size=1, stride=1,
                          norm=norm, activation=activation),
                ConvBNAct(mid, mid, kernel_size=kernel_size, stride=stride,
                          norm=norm, activation=activation),
                ConvBNAct(mid, out_channels, kernel_size=1, stride=1,
                          norm=norm, activation=activation, use_act=False),
            )
        else:
            self.conv = nn.Sequential(
                ConvBNAct(in_channels, out_channels,
                          kernel_size=kernel_size, stride=stride,
                          norm=norm, activation=activation),
                ConvBNAct(out_channels, out_channels,
                          kernel_size=kernel_size, stride=1,
                          norm=norm, activation=activation, use_act=False),
            )

        if stride == 1 and in_channels == out_channels:
            self.shortcut: nn.Module = nn.Identity()
        else:
            # Projection shortcut keeps spatial / channel alignment.
            self.shortcut = ConvBNAct(in_channels, out_channels,
                                       kernel_size=1, stride=stride,
                                       norm=norm, activation=activation,
                                       use_act=False)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.act_out(self.conv(x) + self.shortcut(x))


