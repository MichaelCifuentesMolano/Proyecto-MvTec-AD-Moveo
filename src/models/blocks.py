class UpsampleConv(nn.Module):
    """Bilinear upsample (×scale) followed by a convolutional refinement.

    The bilinear-then-conv pattern is preferred over ``ConvTranspose2d``
    because it avoids checkerboard artifacts and is cleanly representable
    in TensorRT with PTQ/QAT.
    """

    def __init__(self,
                 in_channels: int,
                 out_channels: int,
                 *,
                 scale_factor: float = 2.0,
                 kernel_size: int = 3,
                 norm: NormKind = "batch",
                 activation: ActKind = "relu",
                 mode: Literal["bilinear", "nearest"] = "bilinear",
                 align_corners: bool = False) -> None:
        super().__init__()
        self.scale_factor = float(scale_factor)
        self.mode = mode
        self.align_corners = align_corners if mode == "bilinear" else None
        self.refine = ConvBNAct(in_channels, out_channels,
                                kernel_size=kernel_size, stride=1,
                                norm=norm, activation=activation)

    def forward(self,
                x: torch.Tensor,
                size: tuple[int, int] | None = None) -> torch.Tensor:
        if size is not None:
            x = F.interpolate(x, size=size, mode=self.mode,
                              align_corners=self.align_corners)
        else:
            x = F.interpolate(x, scale_factor=self.scale_factor,
                              mode=self.mode,
                              align_corners=self.align_corners)
        return self.refine(x)


class PixelShuffleUp(nn.Module):
    """Sub-pixel convolution upsampling (Shi et al., 2016).

    Produces sharper edges than bilinear+conv but at a higher parameter
    cost (the conv expands to ``out_channels * upscale_factor**2``).
    """

    def __init__(self,
                 in_channels: int,
                 out_channels: int,
                 *,
                 upscale_factor: int = 2,
                 kernel_size: int = 3,
                 norm: NormKind = "batch",
                 activation: ActKind = "relu") -> None:
        super().__init__()
        if upscale_factor < 1:
            raise ValueError("upscale_factor must be >= 1")
        self.expand = ConvBNAct(
            in_channels,
            out_channels * (upscale_factor ** 2),
            kernel_size=kernel_size, stride=1,
            norm=norm, activation=activation, use_act=False,
        )
        self.shuffle = nn.PixelShuffle(upscale_factor)
        self.act = make_activation(activation)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.act(self.shuffle(self.expand(x)))


class ConvTransposeUp(nn.Module):
    """Transposed convolution upsampling (legacy / fallback).

    Less quantization-friendly than :class:`UpsampleConv`; kept for
    explicit comparisons in the NAS search space.
    """

    def __init__(self,
                 in_channels: int,
                 out_channels: int,
                 *,
                 stride: int = 2,
                 kernel_size: int = 4,
                 norm: NormKind = "batch",
                 activation: ActKind = "relu") -> None:
        super().__init__()
        if kernel_size <= 0:
            raise ValueError("kernel_size must be > 0")
        self.deconv = nn.ConvTranspose2d(
            in_channels, out_channels,
            kernel_size=kernel_size, stride=stride,
            padding=max((kernel_size - stride) // 2, 0),
            bias=(norm == "none"),
        )
        self.norm = make_norm(out_channels, norm)
        self.act = make_activation(activation)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.act(self.norm(self.deconv(x)))


# ---------------------------------------------------------------------------
# Dispatchers (handy for genome-driven model assembly)
# ---------------------------------------------------------------------------
def make_conv_block(in_channels: int,
                    out_channels: int,
                    *,
                    kind: ConvKind = "conv",
                    kernel_size: int = 3,
                    stride: int = 1,
                    norm: NormKind = "batch",
                    activation: ActKind = "relu",
                    expansion: int = 4) -> nn.Module:
    """Build a convolutional stage by name.

    ``kind`` ∈ ``{"conv", "ds", "ir"}`` selects, respectively,
    :class:`ConvBNAct`, :class:`DepthwiseSeparableConv`, and
    :class:`InvertedResidual`. The remaining kwargs cover the union of
    constructor signatures.
    """
    if kind == "conv":
        return ConvBNAct(in_channels, out_channels,
                         kernel_size=kernel_size, stride=stride,
                         norm=norm, activation=activation)
    if kind == "ds":
        return DepthwiseSeparableConv(in_channels, out_channels,
                                      kernel_size=kernel_size, stride=stride,
                                      norm=norm, activation=activation)
    if kind == "ir":
        return InvertedResidual(in_channels, out_channels,
                                kernel_size=kernel_size, stride=stride,
                                expansion=expansion, norm=norm,
                                activation=activation)
    raise ValueError(f"unknown conv kind: {kind!r}")


def make_attention(channels: int,
                   *,
                   kind: AttnKind = "none",
                   reduction: int = 16,
                   spatial_kernel: int = 7,
                   activation: ActKind = "relu") -> nn.Module:
    """Build an attention module by name.

    ``kind`` ∈ ``{"se", "eca", "spatial", "cbam", "none"}``. ``"none"``
    returns ``nn.Identity()`` so the call site can stay branch-free.
    """
    if kind == "se":
        return SEBlock(channels, reduction=reduction, activation=activation)
    if kind == "eca":
        return ECABlock(channels)
    if kind == "spatial":
        return SpatialAttention(kernel_size=spatial_kernel)
    if kind == "cbam":
        return CBAMLite(channels, reduction=reduction,
                        spatial_kernel=spatial_kernel,
                        activation=activation)
    if kind == "none":
        return nn.Identity()
    raise ValueError(f"unknown attention kind: {kind!r}")
