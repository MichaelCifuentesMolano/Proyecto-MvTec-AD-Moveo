# Attention-lite blocks
# ---------------------------------------------------------------------------
class SEBlock(nn.Module):
    """Squeeze-and-Excitation channel attention (Hu et al., 2018).

    Implemented with 1×1 convolutions instead of FC layers for a
    spatial-size-agnostic, quantization-friendly graph.
    """

    def __init__(self,
                 channels: int,
                 reduction: int = 16,
                 activation: ActKind = "relu") -> None:
        super().__init__()
        hidden = max(channels // max(reduction, 1), 4)
        self.pool = nn.AdaptiveAvgPool2d(1)
        self.fc1 = nn.Conv2d(channels, hidden, kernel_size=1, bias=True)
        self.act = make_activation(activation)
        self.fc2 = nn.Conv2d(hidden, channels, kernel_size=1, bias=True)
        self.gate = nn.Sigmoid()

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        s = self.pool(x)
        s = self.act(self.fc1(s))
        s = self.gate(self.fc2(s))
        return x * s


class ECABlock(nn.Module):
    """Efficient Channel Attention (Wang et al., 2020).

    A single 1-D convolution along the channel dimension replaces the
    SE block's two FC layers — ~0 extra parameters and lower latency.
    """

    def __init__(self, channels: int, kernel_size: int = 3) -> None:
        super().__init__()
        _check_kernel(kernel_size)
        self.pool = nn.AdaptiveAvgPool2d(1)
        self.conv = nn.Conv1d(1, 1, kernel_size=kernel_size,
                              padding=kernel_size // 2, bias=False)
        self.gate = nn.Sigmoid()

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        b, c, _, _ = x.shape
        s = self.pool(x).view(b, 1, c)        # [B, 1, C]
        s = self.conv(s)
        s = self.gate(s).view(b, c, 1, 1)
        return x * s


class SpatialAttention(nn.Module):
    """Lightweight spatial attention (CBAM-style spatial path).

    Concats per-channel mean and max maps, applies a single 7×7
    convolution, gates with sigmoid, multiplies the input.
    """

    def __init__(self, kernel_size: int = 7) -> None:
        super().__init__()
        _check_kernel(kernel_size)
        self.conv = nn.Conv2d(2, 1, kernel_size=kernel_size,
                              padding=kernel_size // 2, bias=False)
        self.gate = nn.Sigmoid()

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        avg = x.mean(dim=1, keepdim=True)
        mx = x.amax(dim=1, keepdim=True)
        s = self.gate(self.conv(torch.cat([avg, mx], dim=1)))
        return x * s


class CBAMLite(nn.Module):
    """Channel attention (SE) followed by spatial attention.

    Lighter than full CBAM (no max-pool branch on the channel side),
    suitable for embedded targets while still capturing both
    inter-channel and inter-spatial dependencies.
    """

    def __init__(self,
                 channels: int,
                 reduction: int = 16,
                 spatial_kernel: int = 7,
                 channel_attn: Literal["se", "eca"] = "se",
                 activation: ActKind = "relu") -> None:
        super().__init__()
        if channel_attn == "se":
            self.channel = SEBlock(channels, reduction=reduction,
                                   activation=activation)
        else:
            self.channel = ECABlock(channels)
        self.spatial = SpatialAttention(kernel_size=spatial_kernel)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.spatial(self.channel(x))


